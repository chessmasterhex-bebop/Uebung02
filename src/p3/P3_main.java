package p3;

public class P3_main {
    public static void main(String[] args) {
        // Drei beliebige Werte
        int i = 1;
        int j = 2;
        int k = 3;

        // Vergleiche
        if (i < j) {
            System.out.print("A");
        }

        if (i < k) {
            System.out.print("B");
        }

        if (j < k) {
            System.out.print("C");
        }

        System.out.println(); // Zeilenumbruch am Ende
    }
}
